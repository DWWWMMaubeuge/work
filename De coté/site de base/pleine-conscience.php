<nav id="fil_ariane"><strong>Vous &ecirc;tes ici :</strong> <a href="/">Accueil</a> &gt; Pleine Conscience</nav><h1>Méditation pleine Conscience à Maubeuge<br>
</h1>
<p><strong>La Pleine Conscience expliquée par votre psychologue à Maubeuge</strong>
</p>
<p>Avec la <strong>pleine conscience</strong>, j’apprends à percevoir mes pensées et mes humeurs  comme des nuages traversant  le ciel. Je ne cherche pas à lutter contre elles, ni à les éviter ou à les retenir. Je peux les
    laisser aller et venir. ODC Conseil, votre cabinet de psychologie situé près de <strong>Maubeuge</strong> vous guide dans la compréhension et la pratique de la <strong>méditation pleine conscience</strong>.</p>
<p><strong>Qu’est –ce que la Pleine Conscience ?</strong>
</p>
<a class="lightbox"  href="https://www.odc-conseil.fr/photos/fotolia_24707778_subscription_xxl.jpg"><img class="imgRight" src="https://www.odc-conseil.fr/photos/399x266/fotolia_24707778_subscription_xxl.jpg" title="méditation pleine conscience Maubeuge" alt="méditation pleine conscience Maubeuge" loading="lazy" width="399" height="266" /></a><p>La méditation de pleine conscience, appelée aussi de <strong>pleine présence</strong> ou <strong>Mindfulness</strong>, est un « Etat de conscience qui résulte du fait de porter son  attention, intentionnellement, sur le  moment présent,
    et sans porter de jugement » Jon Kabat-Zinn (2003).</p>
<p>La pleine conscience nous invite à observer simplement ce qui est, ici et maintenant. Elle nous aide à prendre du recul avec nos automatismes et nos filtres mentaux créés par nos conditionnements, nos croyances.</p>
<p>Cette pratique laïque est issue de la tradition bouddhiste. Elle connait un essor grandissant depuis le développement de programmes de réduction du stress et de prévention de la rechute dépressive initiés par Jon Kabat-Zinn
    (professeur de médecine à l’Université du Massachussetts) et Zindel Segal  (professeur de psychologie et psychiatrie à Toronto).</p>
<p><strong>Les bienfaits de la Pleine Conscience</strong>
</p>
<p>Un nombre important d’études validées scientifiquement attestent  de l’intérêt de la méditation de pleine conscience dans le traitement de différents troubles médicaux et psychiques. Une pratique régulière
    entraine une réduction du stress, de l’anxiété, des douleurs chroniques. Elle améliore la qualité du sommeil, renforce les défenses immunitaires, régule la tension artérielle, facilite la gestion des émotions,
    contribue à une meilleure estime de soi.  Elle joue également un rôle important dans la prévention de la rechute dépressive.</p>
<p><strong>Qui peut pratiquer la Pleine Conscience</strong> ?</p>
<p>La méditation de pleine conscience est à la portée de tous. Il n’est, d’ailleurs et heureusement, pas nécessaire de souffrir psychologiquement pour pratiquer la pleine conscience. Toute personne en recherche d’un apaisement
    intérieur peut se tourner vers cette pratique.</p>
<p>En cas de situation de crise (phase dépressive aïgue, troubles psychotiques avec délires ou hallucinations, trouble bi-polaire non stabilisé), il sera préférable d’attendre un retour à une certaine stabilité
    psychique avant d’entreprendre un programme intensif de méditation de pleine conscience.</p>
<p> </p>
<p><strong>Comment pratiquer la Pleine Conscience</strong>
</p>
<p>Méditer ce n’est pas se couper du monde. Ce n’est pas forcément rester immobile en position du lotus. La méditation de pleine conscience se pratique en position statique et en mouvement.</p>
