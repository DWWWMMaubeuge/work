<nav id="fil_ariane"><strong>Vous &ecirc;tes ici :</strong> <a href="/">Accueil</a> &gt; <a href="psychologue-travail-maubeuge.php">Le conseil en entreprise </a> &gt; Sensibilisation </nav><h1>Psychologue du travail et sensibilisation aux risques psychosociaux à Maubeuge</h1>
<p>ODC Conseil, situé à Maubeuge en Sambre Avesnois, intervient auprès des établissements et entreprises de la Région Hauts-de-France. Votre<strong><a href="cabinet-psychologue-maubeuge.php"> psychologue à Maubeuge</a></strong> vous propose
    des <strong>actions de sensibilisation</strong>  <strong>adaptées à vos besoins</strong> sur les thèmes des risques psychosociaux, la qualité de vie au travail, le<a href="stress-au-travail.php" target="_blank"><strong> stress au travail</strong>.</a>
</p>
<p>Vous êtes dirigeant, responsable des ressources humaines ou membre du CHSCT et  êtes à la recherche d’<strong>actions de prévention</strong> visant à <strong>réduire l’impact des risques psychosociaux </strong>sur la santé
    physique et mentale des employés. Faites appel à <strong>ODC Conseil</strong>, <strong>psychologue d'entreprise à Rousies, Hautmont et en Sambre Avesnois.</strong>
</p>
<p>Les sensibilisations visent à <strong>améliorer le bien-être et l’efficacité de vos employés</strong> en leur permettant d’être plus attentifs à certaines problématiques et en les amenant à adopter des comportements
    adaptés à celles-ci.  </p>
<p>S’agissant d’action de prévention des risques professionnels, la sensibilisation peut s’inscrire dans le <strong>DUERP</strong> (Document Unique d’Evaluation des Risques Professionnels)</p>
