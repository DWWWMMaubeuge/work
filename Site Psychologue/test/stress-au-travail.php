                <h2>Qu’est-ce que le stress au travail ?</h2>
                <p>« Un état de stress survient lorsqu’il y a un déséquilibre entre la perception qu’une personne a des contraintes que lui impose son environnement et la perception qu’elle a de ses propres ressources pour y faire
                    face. » (Accord national interprofessionnel 2008).</p>
                <p>Le terme de <strong>stress au travail</strong> est souvent associé à d’autres expressions comme<strong> mal-être</strong> ou <strong>souffrance</strong> <strong>au travail</strong>, <strong>usure professionnelle</strong>, <strong>risques psychosociaux</strong>. On parle également de <strong>trouble de l'adaptation</strong>.</p>
                <p>Pour y voir plus clair, on distingue les<strong> facteurs du stress</strong>, l’<strong>état de stress</strong> et ses <strong>conséquences sur la santé</strong> et l’efficacité du salarié.</p>
                <h2>Quels sont les facteurs de stress au travail ?</h2>
                <p>Les facteurs de stress au travail sont nombreux. Parmi les plus fréquents, nous trouvons une <strong>charge de travail excessive</strong>, une <strong>complexité</strong> des tâches, une absence d’<strong>autonomie</strong>, un <strong>manque de reconnaissance</strong> et
                    de soutien, des <strong>conflits</strong> entre collègues, une <strong>confrontation</strong> à la souffrance d’autrui, des violences et incivilités, une <strong>insécurité</strong> de l’emploi.</p>
                <h2>Quelles sont les conséquences du stress au travail ?</h2>
                <p>La capacité d’adaptation d’un individu à une situation de stress reste limitée. Lorsque le stress devient trop intense et chronique, il peut engendrer des <strong>conséquences d’ordre psychologique</strong> (troubles anxieux
                    ou dépressifs, burnout), d’ordre <strong>comportemental</strong> (irritabilité, repli sur soi, consommation d’alcool ou de drogue), d’ordre<strong> physique</strong> (douleurs, migraines, augmentation des troubles musculo-squelettiques TMS et des
                    maladies cardiovasculaires).</p>
                <p>Le <strong>stress chronique</strong> génère également un<strong> coût pour l’entreprise</strong> de part une<strong> diminution de l’investissement</strong> du salarié, une<strong> diminution de sa vigilance</strong> (risque d’accident plus important), une<strong> augmentation des arrêts maladie</strong>.
                    On parle alors de<strong> conséquences organisationnelles</strong> du stress.</p>
                <h2>Comment prévenir le stress au travail ?</h2>
                <p>Plusieurs types d’actions de prévention du stress existent. On distinguera la prévention dite corrective de la prévention dite primaire.</p>
                <p>- Les actions de<strong> prévention corrective</strong> du stress visent à <strong>réduire les conséquences</strong> du stress sur la personne. Ces actions peuvent prendre la forme de<strong> programme de gestion du stress</strong> (relaxation, méditation),
                    débriefing psychologique suite à un événement grave,<strong> <a href="sensibilisation.php">dispositif d’écoute et de soutien psychologique</a>,</strong> espaces de discussion entre collaborateurs.</p>
                <p>-Les actions de <strong>prévention primaire</strong> sont davantage centrées sur le travail et son organisation. Elles visent à<strong> réduire les facteurs</strong> de stress dans l’entreprise. Ces interventions peuvent s’inscrire dans une
                    démarche plus globale de prévention des Risques Psychosociaux RPS allant du diagnostic jusqu’à la mise en œuvre d’un plan d’action.</p>
