            <h1>Troubles psychiques les plus fréquemment rencontrés chez votre psychologue à Maubeuge<br></h1>
            <div>
                <p>Les <strong>troubles psychiques sont classifiés</strong> selon des critères précis tels que la présence de certains symptômes et leur intensité. Odile Dessailly-Coste,<strong> <a href="Parcours.php">psychologue située près de Maubeuge</a> </strong>vous
                    aide à mieux connaître les<strong> troubles les plus fréquemment</strong> rencontrés en consultation.</p>
                <h2>Les troubles anxieux</h2>
                <h3>Trouble anxieux généralisé (TAG)</h3>
                <p>Le <strong>trouble anxieux généralisé</strong> (TAG) correspond à une<strong> inquiétude excessive</strong> et quasi-permanente qui concerne tous les aspects de la vie quotidienne (travail, famille, argent, enfants…)</p>
                <a class="lightbox" href="https://www.odc-conseil.fr/photos/troubles.jpg"><img class="imgRight" src="images/troubles.jpg" title="Psychologue Maubeuge" alt="Psychologue Maubeuge" loading="lazy" width="400" height="267" /></a>
                <h3>Troubles obsessionnels compulsifs (TOC)</h3>
                <p>Les <strong>troubles obsessionnels compulsifs</strong> (TOC) se manifestent par des peurs particulières qui envahissent la pensée en permanence appelées <strong>obsessions</strong> (peur de la contamination, de la saleté, de faire du mal aux autres...). Afin
                    de réduire son anxiété et neutraliser ses obsessions, le patient a recours à <strong>des rituels</strong>, des comportements répétitifs, appelés <strong>compulsions</strong> (lavage de mains, vérifications, rituels mentaux…)
                    ou adopte une stratégie d’évitement des situations redoutées.</p>
                <h3>Attaque de panique et trouble panique</h3>
                <p>L’<strong>attaque de panique et trouble panique</strong>. L’attaque de panique, appelée également <strong>crise d’angoisse</strong>, correspond à la survenue brutale d’une peur intense, peur de mourir ou d’une catastrophe imminente,
                    peur de perdre de contrôle de soi. Elle se manifeste par des sensations physiques intenses telles que des <strong>vertiges, sensation d’étouffement, palpitations</strong>. On parle de trouble panique lorsqu’il y a répétition des
                    attaques de panique. </p>
                <h3>Phobies</h3>
                <p><strong>Les phobies</strong> sont des peurs irraisonnées et intenses. On distingue les <strong>phobies simples</strong>, dites aussi spécifiques (peurs de l’avion, du chien, de l’araignée…) de la phobie sociale et de l’agoraphobie. La <strong>phobie sociale</strong>, "la
                    peur des autres", est une peur des situations où l’on est exposé au regard d’autrui (peur de rougir, de parler, de manger en public…) L’<strong>agoraphobie</strong> est la peur des espaces découverts, de la foule, ou
                    de toute situation dont on ne peut s’échapper facilement (être seul loin de son domicile, sur un pont, une autoroute…)</p>
                <h3>Etat de stress post traumatique (ESPT)</h3>
                <p>L’<strong>état de stress post traumatique</strong> (ESPT) est un trouble qui peut apparaître suite à un événement "traumatique", c'est-à-dire lorsqu’une personne a été confrontée à la mort, à
                    la peur de mourir, à de graves blessures ou lorsque son intégrité physique ou celle d’une autre personne a été menacée. Cet événement a engendré un<strong> sentiment de peur intense, d’impuissance ou d’horreur</strong>.
                    L’ESPT se manifeste par des pensées ou des cauchemars liés à la scène traumatique, des évitements, une hypervigilance. Le <strong><a href="psychologue-travail-maubeuge.php">débriefing psychologique</a> </strong>permet d’éviter
                    l’apparition d’un stress post-traumatique ou d’en limiter les symptômes.</p>
                <h2>Les troubles du comportement alimentaire </h2>
                <h3>Anorexie mentale </h3>
                <p>L’<strong>anorexie mentale</strong> se caractérise par une<strong> perte de poids très importante</strong> et une perturbation de la relation à l’alimentation et au corps. La personne adopte des <strong>stratégies de contrôle du poids</strong> (vomissements,
                    hyperactivité, absorption de laxatifs…)</p>
                <p>On distingue l’<strong>anorexie restrictive</strong> (l’amaigrissement n’est obtenu que par la restriction alimentaire et l’hyperactivité) et l’<strong>anorexie-boulimie</strong> (présence de crises de boulimie mais recours à des vomissements
                    pour contrôler le poids)</p>
                <h3>Boulimie</h3>
                <p>La <strong>boulimie</strong> se manifeste par la survenue récurrente de<strong> crise de boulimie</strong> (consommation d’une quantité importante de nourriture en un temps limité), le sentiment de<strong> perte de contrôle</strong> (ne pas pouvoir s’arrêter
                    de manger), des comportements compensatoires inappropriés (vomissements provoqués, jeûne, laxatifs, lavements), une préoccupation importante au<strong> sujet du poids et de la silhouette</strong>.</p>
                <h3>Hyperphagie boulimique</h3>
                <p>L’<strong>hyperphagie boulimique</strong> correspond à une <strong>boulimie sans conduite purgative</strong> ou compensatoire s’accompagnant, de fait, d’une prise de poids.</p>
                <h2>Les troubles de l’humeur</h2>
                <h3>Episode dépressif</h3>
                <p>L’<strong>épisode dépressif</strong>, appelé également <strong>dépression caractérisée</strong>, se manifeste par une humeur déprimée (tristesse avec parfois des idées noires, pensées suicidaires), et/ou une <strong>perte d’intérêt</strong> pour
                    ce qu’on aimait auparavant, une incapacité à éprouver du plaisir, un état de tristesse qui se prolonge. Il s’accompagne souvent d’une grande fatigue, de troubles du sommeil, d’un ralentissement général,
                    des difficultés de concentration, un repli sur soi, de l’irritabilité. L’épisode dépressif peut être qualifié de<strong> léger, moyen ou sévère. <br></strong>
                </p>
                <h3>Trouble dysthymique</h3>
                <p>Le <strong>trouble dysthymique</strong> correspond une humeur dépressive présente pratiquement toute la journée, plus d’un jour sur deux pendant au moins deux ans</p>
                <h3>Troubles bipolaires</h3>
                <p>Les <strong>troubles bipolaires</strong> se caractérisent par une<strong> variation anormale de l’humeur</strong> avec une alternance de périodes d’excitation (manie ou hypomanie) et de dépression, voire de mélancolie profonde, entrecoupées
                    de périodes de stabilité.</p>
