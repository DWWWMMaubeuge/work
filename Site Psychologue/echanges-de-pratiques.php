<nav id="fil_ariane"><strong>Vous &ecirc;tes ici :</strong> <a href="/">Accueil</a> &gt; <a href="psychologue-travail-maubeuge.php">Le conseil en entreprise </a> &gt; Échanges de pratiques </nav><h1>Groupes d'échanges et analyse de pratiques à Maubeuge</h1>
<p>Vous êtes employeur et souhaitez <strong>développer les compétences</strong> de vos collaborateurs tout en leur apportant un<strong> soutien</strong>.</p>
<p>Odile Dessailly-Coste,<strong> <a href="cabinet-psychologue-maubeuge.php">psychologue du travail à Maubeuge </a></strong>en Sambre Avesnois (Hauts de France), vous propose l’animation de groupes d’échanges et d’analyse de pratiques avec
    une approche de <strong>codéveloppement</strong>.</p>
<p>Cette  approche de codéveloppement  repose sur l’idée que <strong>chacun peut apprendre à partir de l’expérience des autres</strong>.</p>
<p>En effet,</p>
<ul>
<li>échanger avec d’autres collègues à propos de ses expériences permet des apprentissages impossibles autrement,</li>
    <li>tout ne s’apprend pas en formation,</li>
    <li>un groupe peut trouver lui-même des solutions à ses propres difficultés</li>
</ul>
<p>Les groupes d’échanges et d’analyse de pratiques permettent de<strong> lutter contre l’usure au travail</strong>,<strong> améliorent la cohésion</strong> entre les membres d’une équipe, <strong>développent l’entraide</strong>,<strong> contribuent au partage de savoirs</strong> et
    de savoir-faire.</p>
<h2>Qu’est-ce qu’un groupe d’échanges de pratique ?</h2>
<p>Un groupe d’échanges et d’analyse de pratiques est une approche de formation qui vise à améliorer et consolider la pratique professionnelle des individus.</p>
<p>Il s’agit d’un <strong>moment d’échange entre professionnels</strong> ayant pour objectifs de :</p>
<ul>
<li>Prendre un temps de réflexion sur sa pratique professionnelle</li>
    <li>Apprendre à être plus efficace en trouvant de nouvelles façons d’agir dans sa pratique</li>
    <li>Aider des pairs et être aidé par des pairs</li>
</ul>
<p>Les groupes d'échanges et d'analyse de pratiques s’adressent aussi bien à des <strong>cadres</strong>, des <strong>managers</strong> que des <strong>employés</strong>.</p>
<a class="lightbox"  href="https://www.odc-conseil.fr/photos/echanges.jpg"><img class="imgRight" src="https://www.odc-conseil.fr/photos/400x267/echanges.jpg" title="Psychologue Maubeuge" alt="Psychologue Maubeuge" loading="lazy" width="400" height="267" /></a><p>Les groupes peuvent être mis en place dans<strong> tous les secteurs d’activité</strong> que ce soit auprès des professionnels du secteur<strong> médical</strong> et <strong>médico-social</strong> souvent confrontés à une charge émotionnelle
    importante, ou dans le <strong>secteur industriel</strong>, notamment pour aider les équipes lors de <strong>changements organisationnels</strong>.</p>
<h2>Quel est le rôle de l’animateur d’un groupe d’échanges de pratiques ?</h2>
<p>Le rôle de l'animateur est de<strong> faciliter l’expression des participants</strong>, <strong>favoriser l‘écoute</strong> entre pairs, <strong>réguler les échanges</strong>,<strong> protéger les participants de jugements éventuels</strong> venant des autres
    membres du groupe, <strong>apporter son propre regard</strong> sur la problématique exposée et <strong>donner des repères</strong> aux professionnels.</p>
