<nav id="fil_ariane"><strong>Vous &ecirc;tes ici :</strong> <a href="/">Accueil</a> &gt; <a href="psychologue-maubeuge.php">Psychologie </a> &gt; Les thérapies comportementales et cognitives  </nav><h1>Thérapies comportementales et cognitives utilisées par votre psychologue à Maubeuge</h1>
<p><strong><a href="cabinet-psychologue-maubeuge.php">Psychologue praticienne en thérapie comportementale et cognitive à Maubeuge</a></strong>, région des Hauts de France, Odile Dessailly-Coste vous aide à <strong>surmonter vos difficultés</strong> et
    à <strong>retrouver le bien-être</strong>.</p>
<p>Vous souffrez de dépression, d'angoisse, de phobie sociale, d'un trouble du comportement alimentaire ou de trouble obsessionnel compulsif (TOC).</p>
<p>Vous aimeriez pouvoir en sortir mais ne savez pas comment vous y prendre.</p>
<p>Les thérapies comportementales et cognitives constituent un<strong> traitement efficace</strong> de ces troubles et sont reconnues scientifiquement.</p>
<p>L'aide d'un thérapeute vous permettra de mieux comprendre les mécanismes de votre trouble et d'avancer vers une<strong> diminution de vos symptômes</strong>.</p>
<a class="lightbox"  href="https://www.odc-conseil.fr/photos/cognitives.jpg"><img class="imgRight" src="https://www.odc-conseil.fr/photos/400x397/cognitives.jpg" title="Psychologue Maubeuge" alt="Psychologue Maubeuge" loading="lazy" width="400" height="397" /></a><h2>Les thérapies comportementales et cognitives, qu'est-ce que c'est ?</h2>
<p>Les Thérapies comportementales et cognitives  (TCC) sont des thérapies dites brèves <strong>reconnues et validées scientifiquement</strong>. Elles sont issues des <strong>théories de l’apprentissage</strong>. La thérapie consiste donc
    à <strong>modifier un comportement inadapté</strong> à une situation par un comportement plus adapté qui correspond au souhait du patient.</p>
<p>Le <strong>comportement</strong> d’un individu étant <strong>lié à ses pensées</strong> (cognitions) et à ses <strong>émotions</strong>, les techniques utilisées en thérapie comportementale et cognitive interviennent sur ces 3 niveaux.</p>
<a class="lightbox"  href="https://www.odc-conseil.fr/photos/sans-titre-2-2.jpg"><img class="image center" src="https://www.odc-conseil.fr/photos/800x550/sans-titre-2-2.jpg" title="schéma thérapies comportementales et cognitives" alt="schéma thérapies comportementales et cognitives" loading="lazy" width="800" height="550" /></a><h2>Comment se déroule une thérapie comportementale et cognitive ?</h2>
<p>Les TCC correspondent à une<strong> démarche thérapeutique concrète et active</strong> où le patient apprend à s’observer et à <strong>utiliser des méthodes afin de gérer les situations</strong> qui lui posent problème
    dans la vie quotidienne.</p>
<p>Les objectifs thérapeutiques sont définis avec le patient. Le <strong>thérapeute l'accompagne</strong> dans la mise en place et l’apprentissage de nouvelles stratégies. Les progrès du patient sont régulièrement évalués.</p>
<h2>Les thérapies comportementales et cognitives, pour qui?</h2>
<p>Les Thérapies comportementales et cognitives  offrent des méthodes efficaces, rapides et durables pour le traitement d’un grand nombre de troubles.</p>
<p>Elles s'adressent :</p>
<ul>
<li>aux <strong>enfants</strong> qui présentent, par exemple, une anxiété de séparation, qui se montrent très souvent dans l'opposition avec leurs parents, qui souffrent de phobie scolaire ou encore qui manifestent un trouble autistique.</li>
    <li>aux <strong>adolescents</strong> qui expriment par exemple, un manque de confiance en soi et d'affirmation, une phobie, ou souffre d'anorexie, de boulimie</li>
    <li>aux <strong>adultes</strong> qui présentent l'un des troubles cités précédemment mais aussi des insomnies, une douleur chronique ou une dépendance à l'alcool</li>
    <li>aux<strong> séniors</strong> qui peuvent présenter un état dépressif suite à un départ à la retraite, qui peuvent être confrontés à une maladie ou qui doivent faire face à l'isolement, à une perte d'autonomie.</li>
</ul>
