<h2>Le burn-out : le prévenir ou en sortir avec l'aide du psychologue</h2>
<p><a href="cabinet-psychologue-maubeuge.php"><strong>Psychologue près de Maubeuge</strong></a> en région Hauts de France, Odile Dessailly-Coste, intervient dans le domaine du travail et vous accompagne dans la <strong>mise en place d'une prévention du burn-out</strong> en
    entreprise et aide les salariés à <strong>faire face à l'épuisement professionnel</strong>.</p>
<p>Vous vous sentez dépassé(e) par votre travail, vous êtes de plus en plus irritable, vous manifestez des troubles de la mémoire et des difficultés de concentration, vous n'arrivez plus à récupérer de votre fatigue.
    Votre psychologue à Maubeuge peut vous aider à faire face au burnout.</p>
<a class="lightbox"  href="https://www.odc-conseil.fr/photos/burnout.jpg"><img class="imgRight" src="https://www.odc-conseil.fr/photos/400x267/burnout.jpg" title="Psychologue Maubeuge" alt="Psychologue Maubeuge" loading="lazy" width="400" height="267" /></a><h2>Le burn-out, c'est quoi?</h2>
<p>Le burn-out, ou syndrôme d'épuisement professionnel, correspond à un "<strong>sentiment de fatigue intense</strong>, de<strong> perte de contrôle</strong> et d'<strong>incapacité</strong> à aboutir à des résultats concrets au travail" (Organisation
    Mondiale de la Santé).</p>
<p>Il est la <strong>conséquence d'un stress chronique</strong> issu du travail et de la relation qu'une personne entretient avec lui. Il se manifeste par un<strong> épuisement physique et émotionnel</strong>.</p>
<h2>Comment soigner un burn-out?</h2>
<p>Lorsque le corps a atteint ses limites et qu'un état dépressif s'installe, l'<strong>arrêt maladie</strong> devient indispensable. En effet, la mise à l'écart du travail est nécessaire pour retrouver ses forces, récupérer.
    Parfois,il faut y associer un traitement médicamenteux.</p>
<p>Il est important durant cette période de convalescence, d'être <strong>accompagné par un professionnel</strong> tel que le psychologue. Celui-ci aide la personne à <strong>reprendre confiance en elle</strong>, à<strong> comprendre les raisons</strong> du burn-out,
    et  à<strong> mettre en place des stratégies</strong> pour reprendre une activité professionnelle de façon plus sereine.</p>
<h2>Quelle différence entre burn-out et dépression?</h2>
<p>Il est parfois difficile de distinguer le burn-out d'une dépression. En effet les symptômes sont très proches : fatigue, pertes de concentration et de mémoire, troubles du sommeil, sentiment de dévalorisation, pensées négatives.</p>
<p>Ce qui différencie le burnout d'une dépression est notamment l'<strong>origine de la souffrance</strong>. En effet, la dépression peut avoir des origines variées : séparation, deuil, manque de confiance en soi, ennuis professionnels, histoire
    de vie douloureuse...</p>
<p>Le<strong> burn-out trouve sa source dans le travail</strong>. La personne doit faire face à une surcharge de travail. Souvent d'un tempérament perfectionniste, elle veut relever le défi de répondre à l'urgence et à la charge de travail.
    Elle souhaite souvent ainsi prouver ses compétences et sa valeur et ce, au détriment de sa santé.</p>
<h2>Comment prévenir le burn-out?</h2>
<p>Même si le burn-out reste une manifestation individuelle, l'entreprise peut <strong>mener des actions de prévention auprès de ses salariés</strong>. Celles-ci peuvent prendre la forme d'une<a href="sensibilisation.php"><strong> sensibilisation</strong></a> du
    management à cette problématique, d'un diagnostic de l'organisation de travail afin d'<strong>identifier les facteurs de risque et améliorer la qualité de vie au travail</strong>.</p>
<p>De façon <strong>individuelle</strong>, il est important de<strong> repérer les premiers signes</strong> pouvant conduire au burn-out et veiller à <strong>maintenir un équilibre de vie</strong> entre la sphère <strong>privée</strong> et la sphère <strong>professionnelle</strong>.</p>
