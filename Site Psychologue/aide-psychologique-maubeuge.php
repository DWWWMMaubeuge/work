<nav id="fil_ariane"><strong>Vous &ecirc;tes ici :</strong> <a href="/">Accueil</a> &gt; <a href="psychologue-maubeuge.php">Psychologie </a> &gt; À quoi sert un psychologue ? </nav><h1>Psychologue à Maubeuge, Hauts-de-France </h1>
<p>Il n’y a ni bonne, ni mauvaise raison de consulter un<strong> psychologue</strong>. En effet, nous sommes tous amenés à un moment de notre vie à devoir<strong> faire face à des épreuves</strong> (perte d’un être cher, séparation,
    bouleversement divers), à rencontrer des difficultés personnelles, relationnelles, qui peuvent  nécessiter une aide ponctuelle. </p>
<p>Même si nous pensons parfois pouvoir régler nous-mêmes nos difficultés ou que le temps arrangera les choses, l’<strong>aide du</strong>  <strong>psychologue</strong> peut alléger bien des souffrances.</p>
<p>Vous manifestez des symptômes dépressifs ou anxieux. Vous êtes confronté à des soucis personnels et/ou professionnels. Ceci se répercute sur votre humeur. Vous ne savez pas vers qui vous tourner pour en parler et recevoir
    une <strong>aide neutre et sans jugement</strong>.</p>
<p>Votre <strong>psychologue, Odile Dessailly-Coste, située à Maubeuge</strong> en Sambre Avesnois, vous propose un <strong> temps d'écoute privilégié et bienveillant</strong> dans un cadre garantissant la <strong>confidentialité</strong>. Elle vous apporte
    un soutien, vous accompagne dans la recherche de solutions, vous aide à diminuer les symptômes qui vous gênent au quotidien.</p>
<h2>Psychologue, psychothérapeute, psychiatre, quelle différence ?
</h2>
<p>Le <strong>psychiatre</strong> est un médecin spécialiste des troubles psychiatriques. Sa fonction principale est d'émettre un diagnostic et de délivrer un traitement médicamenteux adapté. </p>
<p>Le <strong>psychologue</strong> possède une formation universitaire spécialisée en psychologie (Master II) : étude scientifique des processus mentaux et comportements humains. Celle-ci lui permet d’accompagner, par une écoute
    et un dialogue, toute personne qui exprime un malaise, une souffrance morale. L'usage du titre de psychologue est protégé depuis 1985.</p>
<p>Le <strong>psychothérapeute</strong> est une personne qui propose des psychothérapies. C'est pourquoi un psychologue ou un psychiatre peuvent être psychothérapeutes. Depuis 2010, le titre est réglementé par la loi.</p>
<h2>Faut-il beaucoup de séances chez le psychologue pour aller mieux ?
</h2>
<p>Le nombre de séances chez le psychologue est très<strong> variable</strong>. Il dépend à la fois de la <strong>problématique</strong> de la personne mais aussi de sa <strong>motivation</strong> et de la relation thérapeutique.</p>
<p>Deux à trois entretiens peuvent être suffisants pour certains patients exprimant simplement un besoin de repères.</p>
<p>Généralement, la durée d'une thérapie varie de quelques mois à une année.</p>
<p>Certaines personnes ressentent le besoin d'un soutien, d'une écoute qui peut durer plusieurs années.</p>
<p>Quoi qu'il en soit, l'objectif de votre <a href="cabinet-psychologue-maubeuge.php"><strong>psychologue à Maubeuge</strong></a>, Odile Dessailly-Coste, est de vous amener à <strong>être le plus rapidement et le plus durablement autonome dans la gestion des difficultés que vous rencontrez</strong>.</p>
<h2>Quelle est la durée d'un entretien chez le psychologue ?
</h2>
<p>Votre <strong>psychologue à Maubeuge</strong>, Odile Dessailly-Coste, vous accompagne lors d'entretiens individuels d'une durée d<strong>'environ 45 mn à 1h</strong>.</p>
<p>La fréquence des séances est généralement de 15 jours mais peuvent varier selon vos besoins.</p>
<h2>Quel est le coût d’une séance chez votre psychologue ?</h2>
<p>Pour une<strong> consultation individuelle</strong>, le tarif de la séance est de <strong>50</strong><strong>€</strong>.
    <br>Les séances chez le psychologue ne sont pas remboursées par la sécurité sociale mais<strong> certaines mutuelles proposent une prise en charge</strong>.</p>
