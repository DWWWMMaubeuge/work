<nav id="fil_ariane"><strong>Vous &ecirc;tes ici :</strong> <a href="/">Accueil</a> &gt; Le conseil en entreprise </nav><h1>Le conseil en entreprise - Psychologue du travail à Maubeuge</h1>
<h2>Un salarié heureux est un salarié plus efficace</h2>
<a class="lightbox"  href="https://www.odc-conseil.fr/photos/conseil.jpg"><img class="imgLeft" src="https://www.odc-conseil.fr/photos/300x200/conseil.jpg" title="Psychologue Maubeuge" alt="Psychologue Maubeuge" loading="lazy" width="300" height="200" /></a><p>Il est désormais reconnu que la<strong> qualité de vie au travail</strong> (QVT), l’amélioration des <strong>conditions de travail</strong> et le <strong>bien-être</strong> des salariés contribuent à la <strong>performance d’une entreprise</strong>.</p>
<p>ODC conseil, cabinet spécialisé dans le domaine des<strong> Risques psychosociaux</strong> RPS, la gestion du <strong><a href="stress-au-travail.php">stress au travail</a> </strong>et l’amélioration de la <strong>qualité de vie au travail</strong>, vous accompagne
    dans la mise en oeuvre de votre <strong>démarche globale de prévention</strong> et vous propose également des <strong>actions spécifiques</strong> telles que :</p>
<ul>
<li>Les <strong>Consultations "Souffrance et Travail"</strong> : les entretiens individuels permettent aux salariés en situation de souffrance (<strong><a href="burn-out-maubeuge.php">burn out</a></strong>, rumination, conflits,…)  de recevoir une écoute
        et des repères afin de faire face aux difficultés qu'ils rencontrent. Les managers peuvent avoir recours à ce dispositif d'écoute et d'accompagnement dans le but de recevoir des conseils notamment sur des problématiques
        de management.</li>
    <li>Le <strong>Débriefing psychologique</strong> : face à un événement grave et traumatisant (accident, agression, braquage, suicide ou décès d'un salarié), il est indispensable de permettre au personnel de verbaliser ses émotions
        à propos de l'expérience à laquelle il vient d'être confronté. Le débriefing psychologique vise à prévenir ou diminuer l'apparition de symptômes de stress post-traumatiques (troubles du sommeil, angoisses,
        souvenirs intrusifs...)</li>
    <li>Les <strong>Espaces de discussion</strong> : proposer un lieu d'expression où les salariés peuvent parler de leur travail, de la manière de le réaliser permet de résoudre des problèmes ponctuels et contribue à améliorer
        le fonctionnement des équipes et de l'entreprise. </li>
</ul>
<div class="clear_both"></div>
<p>La cabinet de votre <a href="cabinet-psychologue-maubeuge.php">psychologue à Maubeuge</a>, ODC Conseil, est agréé IPRP <strong>Intervenant en Prévention des Risques Professionnel</strong> par la DIRECCTE </p>
<p>Votre psychologue intervient sur <strong>Maubeuge</strong>, la <strong>Sambre Avesnois</strong>, et l'<strong><strong>Aisne (Hirson, Laon, La Capelle, St Quentin),</strong></strong>en <strong>Région des Hauts de France</strong> (ex Nord-pas-de-Calais-Picardie).</p>
<h2>
Mes interventions, quelques exemples :</h2>
<ul>
<li>Dans un contexte de réorganisation d’une multinationale de la filière nucléaire,  accompagnement du personnel d’un des sites industriels par un dispositif d’écoute et l’animation de groupes d’échanges
        de pratiques auprès de managers,</li>
    <li>Suite à un braquage avec arme dans un  supermarché hard-discount, débriefing psychologique et entretiens de soutien auprès des employés libre service ayant été victimes,</li>
    <li>Dans le cadre d’un plan social au sein d’un laboratoire pharmaceutique, accompagnement d’une partie du personnel par l’animation de groupes de parole.</li>
</ul>
